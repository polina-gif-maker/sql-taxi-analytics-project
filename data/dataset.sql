# Описание данных

В проекте используются синтетические данные, сгенерированные для имитации работы сервиса такси.

## Таблицы

### users — пользователи
Содержит информацию о зарегистрированных пользователях.

Поля:
- user_id — уникальный идентификатор пользователя
- registration_date — дата регистрации
- city — город пользователя

---

### rides — поездки
Содержит информацию о поездках пользователей.

Поля:
- ride_id — уникальный идентификатор поездки
- user_id — ссылка на пользователя
- driver_id — идентификатор водителя
- ride_date — дата и время поездки
- distance_km — расстояние поездки
- fare_amount — стоимость поездки
- status — статус поездки (completed / cancelled)

---

## Примечание

Данные являются синтетическими и используются в учебных целях.

Набор данных минимален и предназначен для демонстрации SQL-запросов и аналитических подходов.
